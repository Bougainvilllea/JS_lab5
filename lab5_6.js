// Объявление асинхронной функции wait
async function wait() {
    // Ожидание выполнения Promise, который завершится через 1000 мс (1 секунду)
    // new Promise создает новый промис, setTimeout откладывает выполнение
    // resolve - функция, которая выполнится через указанное время
    await new Promise(resolve => setTimeout(resolve, 1000));
    
    // Возврат значения 10 после завершения ожидания
    // Это значение будет обернуто в Promise автоматически (так как функция async)
    return 10;
}

// Объявление синхронной функции f
function f() {
    // Вызов асинхронной функции wait()
    // Метод then() регистрирует колбэк, который выполнится после разрешения промиса
    // r - значение, возвращенное из wait() (в данном случае 10)
    wait().then(r => console.log(r));
}

// Вызов функции f
f();
